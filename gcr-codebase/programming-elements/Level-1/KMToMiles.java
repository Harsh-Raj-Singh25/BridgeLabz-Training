public class KMToMiles{
    public static void main(String [] args){
	    
        double DISTANCE=10.8; // in kilometers
        // converting distance from kilometers to miles
        double NEW_DISTANCE= 10.8 * 1.6;

        // Printing the converted Distance
        System.out.println("The distance " + DISTANCE+ " km in miles is "+ NEW_DISTANCE);		
	}
}